# • Alguns métodos de lista em Python
# • append(elemento): Cria um novo espaço na lista e coloca elemento nesse
# espaço. Aumente em 1 a quantidade de elementos na lista
# • insert(indice,elemento): Cria um novo espaço na lista, no índice indice,
# deslocando todos os elementos a partir de indice para a direita e coloca
# elemento em indice.
# • count(elemento): Retorna a quantidade de vezes que elemento ocorre na lista.
# • index(elemento): Retorna o índice onde está localizada primeira ocorrência de
# elemento.
# • remove(elemento): Remove a primeira ocorrência de elemento da lista.
# • clear(): Remove todos os elementos da lista.
# • sort(): Ordena os elementos da lista em ordem não decrescente.
# • reverse(): Inverte a ordem dos elementos da lista. O primeiro troca de lugar com
# o último, o segundo com o penúltimo e assim sucessivamente.

# • Algumas funções usadas em listas
# • len(l): Retorna a quantidade de elementos na lista.
# • max(l): Retorna o maior elemento na lista.
# • min(l): Retorna o menor elemento na lista.
# • sum(l): Retorna a soma dos elementos na lista.
# • sorted(l): Retorna um nova lista com os elementos
# ordenados não-decrescentemente.